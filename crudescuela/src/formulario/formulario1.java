    public void eliminardatos(){
            
      int filaseleccionada=tbalumnos.getSelectedRow();
      
        try {
            String sql="delete from alumnos where idalumnos="+tbalumnos.getValueAt(filaseleccionada, 0);
            Statement st=CON.createStatement();
            int n=st.executeUpdate(sql);
            
            if (n>=0){ //ciclo para detyermianr si la sentecia se ejecuta
                JOptionPane.showMessageDialog(null,"eliminacion exitosa");
            }
            
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"eliminacion fallida"+ e.getMessage());
        }

     
    }
    
    
    public void mostrardatos(){
        String[] titulos={"iD","Nombre","Apellido","Materia","Calificacion","Status"};
        String[] registro= new String[7]; 
        
        DefaultTableModel  modelo=new DefaultTableModel(null,titulos);
        String SQL="select * from alumnos";
        
        try{
        Statement st=CON.createStatement();
        ResultSet rs=st.executeQuery(SQL);
        
        while(rs.next()){
        registro[0]=rs.getString("idalumnos");
        registro[1]=rs.getString("nombre");
        registro[2]=rs.getString("apellido");
        registro[3]=rs.getString("materia");
        registro[4]=rs.getString("calificacion");
        registro[5]=rs.getString("estatus");

        modelo.addRow(registro);}
         
         tbalumnos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"error al mostar datos"+e.getMessage());
        }
        
     } 
    //metodo para mostar datos filtrados por un dato de la tabla
      public void filtrardatos(String valor){
        String[] titulos={"iD","Nombre","Apellido","Materia","Calificacion","Status"};
        String[] registro= new String[7]; 
        
        DefaultTableModel  modelo=new DefaultTableModel(null,titulos);
        String SQL="select * from alumnos where nombre like '%"+valor+"%'";
        
        try{
        Statement st=CON.createStatement();
        ResultSet rs=st.executeQuery(SQL);
        
        while(rs.next()){
        registro[0]=rs.getString("idalumnos");
        registro[1]=rs.getString("nombre");
        registro[2]=rs.getString("apellido");
        registro[3]=rs.getString("materia");
        registro[4]=rs.getString("calificacion");
        registro[5]=rs.getString("estatus");

        modelo.addRow(registro);}
         
         tbalumnos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"error al mostar datos"+e.getMessage());
        }
        
     } 
    
     public void actualizarDatos(){
        try {
            String sql=("update alumnos set nombre=?,apellido=?,materia=?,calificacion=?,estatus=? where idalumnos=?"); 
            
            int filaseleccioanda=tbalumnos.getSelectedRow();
            String dao=(String)tbalumnos.getValueAt(filaseleccioanda,0);  //PARA INDICAR LA LINEA SELECCIONADA
            PreparedStatement pt=CON.prepareStatement(sql);
            pt.setString(1,tx_nombre.getText());
            pt.setString(2,tx_apellido.getText());
            
            int seleccionar=txtmateria.getSelectedIndex();
            pt.setString(3,txtmateria.getItemAt(seleccionar));
            
            pt.setDouble(4,Double.parseDouble(tx_calificacion.getText()));
            int seleccionar2=txtestatus.getSelectedIndex();
            pt.setString(5,txtestatus.getItemAt(seleccionar2));
            
            pt.setString(6,dao);
            pt.execute();
            
            JOptionPane.showMessageDialog(null,"Actualizacion exitoso");

        } catch (Exception e) {
           JOptionPane.showMessageDialog(null,"Actualizacion fallido"+ e.getMessage());
        }
    }
    
    
    
    
    
    
    
